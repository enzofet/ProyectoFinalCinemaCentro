    private void rellenarTablaPelicula() {

        try {
            List<Pelicula> pelis = peliculaDAO.listarTodasPeliculas();
            DefaultTableModel model = VentanaAdministrativo.armarCabeceras(cabeceras);
            for (Pelicula p : pelis) {
                model.addRow(new Object[]{
                    p.getId_Pelicula(),
                    p.getTitulo()
                });
            }
            tblpeliculas.setModel(model);
            ocultarID();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla peliculas: " );
        }
    }

    private void rellenarTablaSala() {
        try {
            List<Sala> salas = salaDAO.listarsalas();
            DefaultTableModel model = VentanaAdministrativo.armarCabeceras(cabecerass);
            for (Sala s : salas) {
                model.addRow(new Object[]{
                    s.getNro_Sala()
                });
            }
            jTableSala.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla salas : " );
        }
    }
    
