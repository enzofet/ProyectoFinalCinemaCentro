    private void jbtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAgregarActionPerformed
        // TODO add your handling code here:

        try {
            List<Cliente> lista = maniCliente.listarClientes();

            if (txtDNI.getText().isEmpty() || txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty()
                    || txtFechaNac.getText().isEmpty() || txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos los campos deben completarse.");
                return;
            }
            if (txtDNI.getText().length() < 7 || txtDNI.getText().length() > 8) {
                JOptionPane.showMessageDialog(this, "Debe ingresar un DNI válido (7 u 8 dígitos).");
                txtDNI.setText("");
                return;
            } else {

                int dni = Integer.parseInt(txtDNI.getText());
                String nombre = txtNombre.getText();
                String apellido = txtApellido.getText();
                String fecha = txtFechaNac.getText();
                String pass = txtPassword.getText();
                boolean estado = true;
                LocalDate fechaNac;
                LocalDate fechaMin = LocalDate.of(1900, 1, 1);
                LocalDate fechaMax = LocalDate.now().minusYears(18);

                try {
                    fechaNac = LocalDate.parse(fecha, dtf);

                    if (fechaNac.isBefore(fechaMin) || fechaNac.isAfter(fechaMax)) {
                        JOptionPane.showMessageDialog(this, "La fecha debe ser entre '01-01-1900' y '" + fechaMax.format(dtf) + "'.");
                        txtFechaNac.setText("");
                        return;
                    }
                } catch (DateTimeParseException e) {
                    JOptionPane.showMessageDialog(this,
                            "La fecha debe estar en formato dd-MM-yyyy y ser válida.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                    placeholderFecha();
                    return;
                }
                for (Cliente client : lista) {
                    if (client.getDni() == dni) {
                        JOptionPane.showMessageDialog(this, "Ya existe un cliente con ese  DNI: " + client.getDni());
                        txtDNI.setText("");
                        return;
                    }
                }

                Cliente cliente = new Cliente(dni, fechaNac, nombre, apellido, pass, estado);

                maniCliente.agregarCliente(cliente);
                clear();

                if (estadoCompra) {
                    int id_cliente = cliente.getId_cliente();
                    venta.setId_Cliente(id_cliente);
                    JOptionPane.showMessageDialog(this, "Cliente agregado exitosamente.");
                    this.dispose();

                } else {
                    Object[] opciones = {"Si", "No"};
                    int eleccion = JOptionPane.showOptionDialog(
                            null, //null para centrar
                            "Cliente agregado exitosamente\n¿Desea agregar otro cliente?",
                            "",
                            JOptionPane.DEFAULT_OPTION,
                            JOptionPane.QUESTION_MESSAGE,
                            null,
                            opciones,
                            opciones[0]);

                    if (eleccion == 1) {
                        this.dispose();
                    }
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Dato Incorrecto, se espera un DNI");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "ocurrio un error " + e.getMessage());
        }
    }//GEN-LAST:event_jbtAgregarActionPerformed

    private void jbtSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalirActionPerformed
        // TODO add your handling code here:
        if (txtDNI.getText() == null && txtNombre == null
                && txtApellido == null && txtFechaNac == null
                && txtPassword == null) {
            this.dispose();
        } else {
            Object[] opciones = {"Si", "No"};
            int eleccion = JOptionPane.showOptionDialog(
                    null,
                    "Carga de clientes en curso. \n¿Está seguro que desea salir?",
                    "",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    opciones,
                    opciones[0]);

            if (eleccion == 0) {
                this.dispose();
            } else if (eleccion == 1) {
                return;
            }
        }
    }//GEN-LAST:event_jbtSalirActionPerformed

