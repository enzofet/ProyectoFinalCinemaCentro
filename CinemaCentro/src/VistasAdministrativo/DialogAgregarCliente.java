    private void placeholderFecha() {
        txtFechaNac.setText("dd-MM-yyyy");
        txtFechaNac.setForeground(Color.GRAY);
        txtFechaNac.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent e) {
                if (txtFechaNac.getText().equals("dd-MM-yyyy")) {
                    txtFechaNac.setText("");
                    txtFechaNac.setForeground(Color.BLACK);
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent e) {
                if (txtFechaNac.getText().equals("")) {
                    txtFechaNac.setText("dd-MM-yyyy");
                    txtFechaNac.setForeground(Color.GRAY);
                }
            }
        });
    }

